public class switch_ {
    public static void main(String args[]) {

        // how to use switch

        // switch (variable){
        //     case 1:
                    // break;
        //     case 2:
                    // break;
        //     case 3:
                    // break;
        //     default:
        // }
        int number = 2;
        switch(number) {
            case 1: System.out.println("samosa");
            break;
            case 2: System.out.println("pav bhaji");
            break;
            case 3: System.out.println("burger");
            break;
            default: System.out.println("paani");
    }
}
}
